		function intersect(obj1, obj2) {
			var obj1W = obj1.nominalBounds;
			var obj2W = obj2.nominalBounds;
		
			if (obj1.x + (obj1W.width / 15) <= obj2.x - (obj2W.width / 15)) {
				return false;
			}
		
			if (obj1.y + (obj1W.height / 15) <= obj2.y - (obj2W.height / 15)) {
				return false;
			}
		
			if (obj1.x - (obj1W.width / 15) > obj2.x + (obj2W.width / 15)) {
				return false;
			}
		
			if (obj1.y - (obj1W.height / 15) > obj2.y + (obj2W.height / 15)) {
				return false;
			}
			return true;
		}
		
		
